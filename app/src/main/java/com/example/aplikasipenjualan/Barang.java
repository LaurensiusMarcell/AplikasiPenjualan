package com.example.aplikasipenjualan;

public class Barang {
    private String kode_bar;
    private String nama_bar;
    private String sat_bar;
    private String jum_bar;
    private String hrg_bar;
    private String key;
    private String jual_bar;
    private String total_bar;

    public String getKode_bar() {
        return kode_bar;
    }

    public void setKode_bar(String kode_bar) {
        this.kode_bar = kode_bar;
    }

    public String getNama_bar() {
        return nama_bar;
    }

    public void setNama_bar(String nama_bar) {
        this.nama_bar = nama_bar;
    }

    public String getSat_bar() {
        return sat_bar;
    }

    public void setSat_bar(String sat_bar) {
        this.sat_bar = sat_bar;
    }

    public String getJum_bar() {
        return jum_bar;
    }

    public void setJum_bar(String jum_bar) {
        this.jum_bar = jum_bar;
    }

    public String getHrg_bar() {
        return hrg_bar;
    }

    public void setHrg_bar(String hrg_bar) {
        this.hrg_bar = hrg_bar;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getJual_bar() {
        return jual_bar;
    }

    public void setJual_bar(String jual_bar) {
        this.jual_bar = jual_bar;
    }

    public String getTotal_bar() {
        return total_bar;
    }

    public void setTotal_bar(String total_bar) {
        this.total_bar = total_bar;
    }
}
